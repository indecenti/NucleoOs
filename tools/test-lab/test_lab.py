    @staticmethod
    def _train_questions(txt):
        """Estrae la lista delle domande (q) dal testo: array JSON / JSONL / una per riga."""
        txt = txt.strip()
        if not txt:
            return []
        out = []
        try:
            d = json.loads(txt)
            if isinstance(d, list):
                for o in d:
                    if isinstance(o, dict) and o.get("q"): out.append(str(o["q"]).strip())
                    elif isinstance(o, str) and o.strip(): out.append(o.strip())
                return out
        except Exception:
            pass
        for l in txt.splitlines():
            l = l.strip()
            if not l or l.startswith("//"):
                continue
            if l.startswith("{"):
                try:
                    o = json.loads(l)
                    if o.get("q"): out.append(str(o["q"]).strip()); continue
                except Exception:
                    pass
            out.append(l)
        return out

