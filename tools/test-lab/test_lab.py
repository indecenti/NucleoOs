    def run(self, ids):
        if self.worker and self.worker.is_alive():
            self.status.config(text="già in esecuzione…"); return
        ids = [i for i in ids if i in self.tests]
        if not ids: return
        self.prev_health = self._compute_health() if any(t["status"] != "pending" for t in self.tests.values()) else self._last_snapshot()
        for i in ids: self.tests[i].update(status="pending", summary="")
        self._sync_rows()
        self.stop_flag.clear()
        self._device_ip_snap = self.device_ip.get().strip()   # snapshot on the main thread (Tk vars aren't thread-safe)
        try: self._device_delay_snap = max(3, int(self.device_delay.get()))   # floor 3s, anche se l'utente digita meno
        except (tk.TclError, ValueError): self._device_delay_snap = 12
        self._last_ids = list(ids)        # esattamente cosa è girato in QUESTA corsa → ambito del report
        self._full_run = len(ids) >= 20   # a substantial run (ANIMA-offline / NL / all) records a trend point
        self.worker = threading.Thread(target=self._run_batch, args=(ids,), daemon=True); self.worker.start()

    @staticmethod
    def _is_device(t):
        """Un test colpisce il Cardputer reale se è device-load O ha il flag ui_device (es. allenamento)."""
        return t.get("category") == "device-load" or bool(t.get("ui_device"))

    def _args_for(self, t):
        """Args del registry, più --url <device-ip> per i test che toccano il device (e --delay se ui_delay).
        L'IP/pausa digitati (snapshot sul main thread) sovrascrivono i default dello script; vuoto = li lascia."""
        args = list(t["args"])
        if self._is_device(t) and self._device_ip_snap and "--url" not in args:
            ip = self._device_ip_snap
            args += ["--url", ip if ip.startswith("http") else f"http://{ip}"]
        if t.get("ui_delay") and "--delay" not in args:
            args += ["--delay", str(self._device_delay_snap)]   # pausa/cadenza impostata dalla UI
        return args

    def _stream_proc(self, t, to, on_line):
        """Esegue il test e ne fa lo STREAM dello stdout riga-per-riga (on_line per ogni riga), invece di
        catturare tutto alla fine. Ritorna (returncode, output_completo, timed_out). PYTHONUNBUFFERED forza
        il figlio Python a non bufferizzare lo stdout su pipe (altrimenti niente live). ■ Stop uccide il proc."""
        env = {**os.environ, "PYTHONUTF8": "1", "PYTHONIOENCODING": "utf-8", "PYTHONUNBUFFERED": "1"}
        proc = subprocess.Popen([t["cmd"], *self._args_for(t)], cwd=REPO, stdout=subprocess.PIPE,
                                stderr=subprocess.STDOUT, text=True, bufsize=1, encoding="utf-8",
                                errors="replace", env=env)
        self._cur_proc = proc                   # esposto a stop(): kill IMMEDIATO anche durante una pausa lunga
        timed = {"v": False}
        timer = None
        if to:                                  # to=None (registry timeout_s:null) → nessun timer, soak illimitato
            def _kill():
                timed["v"] = True
                try: proc.kill()
                except Exception: pass
            timer = threading.Timer(to, _kill); timer.start()
        lines = []
        try:
            for line in proc.stdout:
                line = line.rstrip("\n")
                lines.append(line)
                on_line(line)
                if self.stop_flag.is_set():
                    try: proc.kill()
                    except Exception: pass
                    break
        finally:
            try: proc.stdout.close()
            except Exception: pass
            rc = proc.wait()
            if timer: timer.cancel()
            self._cur_proc = None
        return rc, "\n".join(lines), timed["v"]

    def _run_batch(self, ids):
        if self.rebuild.get():
            self.q.put(("status", "ricompilo anima.exe…"))
            try: subprocess.run(["node", "tools/anima-host/anima.mjs", "--ensure"], cwd=REPO, capture_output=True, text=True, timeout=600)
            except Exception as e: self.q.put(("status", f"build: {e}"))
        t0 = time.time()
        for i in ids:
            if self.stop_flag.is_set(): self.q.put(("status", "interrotto")); break
            t = self.tests[i]; self._clear_volatile()
            tgt = f"  📡 → {self._device_ip_snap or 'default'}" if self._is_device(t) else ""
            self.q.put(("set", i, "running", "")); self.q.put(("status", f"▶ {t['label']}{tgt}"))
            self.q.put(("livestart", i))
            status = "error"; ts0 = time.time()
            to = t["timeout_s"] if "timeout_s" in t else 900   # registry può dare null = nessun timeout (soak lunghi)
            try:
                rc, out, timed_out = self._stream_proc(t, to, lambda ln, _i=i: self.q.put(("line", _i, ln)))
                last = next((ln for ln in reversed(out.splitlines()) if ln.strip()), "")
                if self.stop_flag.is_set():
                    self.q.put(("done", i, "error", "interrotto", out, time.time() - ts0))
                elif timed_out:
                    self.q.put(("done", i, "error", "timeout", out, time.time() - ts0))
                else:
                    status = "skip" if ("SKIP" in out and rc == 0) else ("pass" if rc == 0 else "fail")
                    self.q.put(("done", i, status, last.strip()[-90:], out, time.time() - ts0))
            except Exception as e:
                self.q.put(("done", i, "error", str(e)[:80], str(e), time.time() - ts0))
            # Cooldown solo fra test device che hanno DAVVERO girato (no skip), e mai dopo l'ultimo.
            if (t.get("category") == "device-load" and status in ("pass", "fail")
                    and i != ids[-1] and not self.stop_flag.is_set()):
                for s in range(DEVICE_COOLDOWN_S, 0, -1):
                    if self.stop_flag.is_set(): break
                    self.q.put(("status", f"raffreddamento device {s}s…")); time.sleep(1)
        self.q.put(("status", f"fatto in {time.time()-t0:.1f}s"))
        self.q.put(("batchdone", None))

    def stop(self):
        self.stop_flag.set(); self.status.config(text="stop richiesto…")
        p = getattr(self, "_cur_proc", None)        # uccide SUBITO il processo in corso (no attesa fine pausa)
        if p and p.poll() is None:
            try: p.kill()
            except Exception: pass

    @staticmethod
    def _clear_volatile():
        sd = os.path.join(REPO, "tools", "anima-host", "sd", "data", "anima")
        for f in ("units.txt", "session.txt", "telemetry.ndjson", "user.tsv", "user.vec", "user.tsv.tmp",
                  "user.vec.tmp", "profile.tsv", "profile.tsv.tmp", "events.tsv", "events.txt", "calendar.tsv"):
            try: os.remove(os.path.join(sd, f))
            except OSError: pass

    # ---- queue drain ---------------------------------------------------------------------------
    def _drain(self):
        try:
            while True:
                msg = self.q.get_nowait()
                if msg[0] == "status": self.status.config(text=msg[1])
                elif msg[0] == "livestart": self._live_begin(msg[1])
                elif msg[0] == "line": self._live_line(msg[1], msg[2])
                elif msg[0] == "set":
                    _, i, st, su = msg; self.tests[i].update(status=st, summary=su); self._sync_one(i)
                elif msg[0] == "done":
                    _, i, st, su, out, dur = msg
                    self.tests[i].update(status=st, summary=su, output=out, duration=dur); self._sync_one(i)
                    self._live_finish(i, st, su)
                    self._refresh_progress()
                elif msg[0] == "batchdone":
                    self._live_idle()
                    self._render_dashboard()
                    if getattr(self, "_full_run", False): self._append_history()
                    try:
                        rpt = self._write_report(getattr(self, "_last_ids", []))
                        if rpt: self.status.config(text=f"{self.status.cget('text')}  ·  report: {os.path.relpath(rpt, REPO)}")
                    except Exception as e:
                        self.status.config(text=f"{self.status.cget('text')}  ·  report KO: {e}")
        except queue.Empty:
            pass
        self.after(80, self._drain)

    def _refresh_progress(self):
        done = sum(1 for t in self.tests.values() if t["status"] in ("pass", "fail", "error", "skip"))
        self.prog["maximum"] = len(self.tests); self.prog["value"] = done

    def _row_of(self, tid):
        for it, i in self.item_for.items():
            if i == tid: return it
        return None

    def _sync_one(self, tid):
        it = self._row_of(tid)
        if it: self.tree.item(it, values=(DOT[self.tests[tid]["status"]], self.tests[tid]["summary"]), tags=(self.tests[tid]["status"],))

    def _sync_rows(self):
        for tid in self.tests: self._sync_one(tid)
        self._refresh_progress()

    @staticmethod
    def _mmss(sec):
        sec = int(max(0, sec)); return f"{sec // 60:02d}:{sec % 60:02d}"

    def _live_begin(self, tid):
        """Un test parte: porta in vista lo stream e azzera l'intestazione. I riquadri contatori restano
        nascosti finché non arrivano righe per-query (li mostra solo il NL soak); per gli altri test la
        barra resta indeterminata (animata) e il log scorre."""
        try: self.nb.select(self.tab_test)
        except Exception: pass
        t = self.reg_tests.get(tid, {})
        self._live_tid = tid
        self._live_active = True
        self._live_has_q = False
        self._live_t0 = time.time()
        self._live_label = t.get("label", "")
        self._live_stats = {"served": 0, "crash": 0, "err": 0, "trap": 0, "n": 0, "tot": 0}
        dev = self._is_device(t)
        self.live_title.config(text=f"▶ {self._live_label}", fg=CL["warn"] if dev else CL["fg"])
        self.live_verdict.config(text="")
        self._cells.pack_forget()                       # riquadri nascosti finché non c'è una riga [q]
        try: self.live_prog.config(mode="indeterminate"); self.live_prog.start(60)
        except tk.TclError: pass
        self.out.delete("1.0", "end")
        self._render_live()

    def _live_color(self, line):
        u = line.upper()
        if line.startswith("[q] "):
            return "bad" if (" CRASH " in line or " H4" in line or " H5" in line) else \
                   ("warn" if "TRAPPOLA-NON-ASTENUTA" in u else "good")
        if "FAIL" in u or "ABORT" in u or "CRASH" in u: return "bad"
        if "PASS" in u or "LIVENESS OK" in u: return "good"
        if "SKIP" in u or "WARN" in u: return "warn"
        return "dim"

    def _live_update_stats(self, line):
        """Parsa '[q] i/N mark lang tier ms · query' → aggiorna SOLO i contatori (il rendering è separato)."""
        if not line.startswith("[q] "):
            return False
        try:
            tok = line.split(" · ", 1)[0].split()       # ['[q]','i/N','mark','lang','tier','msf']
            i, tot = tok[1].split("/")
            s = self._live_stats
            s["n"], s["tot"] = int(i), int(tot)
            mark = tok[2]
            if mark == "ok": s["served"] += 1
            elif mark == "CRASH": s["crash"] += 1
            elif mark.startswith("H"): s["err"] += 1
            if "trappola-non-astenuta" in line.lower(): s["trap"] += 1
            return True
        except (ValueError, IndexError):
            return False

    def _render_live(self):
        """Disegna l'intestazione viva: badge stato + indicatore globale (lampeggia) + riquadri + barra + meta.
        Chiamato sia all'arrivo di una riga sia dal ticker (così l'orologio avanza fra una query e l'altra)."""
        active = getattr(self, "_live_active", False)
        sym = "●" if getattr(self, "_run_blink", False) else "○"
        s = getattr(self, "_live_stats", {"served": 0, "crash": 0, "err": 0, "trap": 0, "n": 0, "tot": 0})
        el = time.time() - getattr(self, "_live_t0", time.time())
        if active:
            self.live_badge.config(text="ESECUZIONE", fg=CL["warn"])
            tail = f"  ·  {s['n']}/{s['tot']}" if self._live_has_q and s["tot"] else ""
            self.runpill.config(text=f"{sym} {self._live_label}{tail}", fg=CL["warn"])
        if self._live_has_q:
            for k in ("served", "crash", "err", "trap"):
                self.cell_val[k].config(text=str(s[k]))
            eta = (el / s["n"]) * (s["tot"] - s["n"]) if s["n"] else 0
            pct = (100 * s["n"] // s["tot"]) if s["tot"] else 0
            try: self.live_prog.config(value=s["n"], maximum=max(1, s["tot"]))
            except tk.TclError: pass
            self.live_meta.config(text=f"{s['n']}/{s['tot']} ({pct}%)   ETA {self._mmss(eta)}   trascorso {self._mmss(el)}")
        elif active:
            self.live_meta.config(text=f"in esecuzione…   trascorso {self._mmss(el)}")

    def _live_finish(self, tid, status, summary):
        """A test finito: ferma la barra, mostra il verdetto evidenziato (se è il test mostrato live)."""
        if getattr(self, "_live_tid", None) != tid:
            return
        try: self.live_prog.stop(); self.live_prog.config(mode="determinate")
        except tk.TclError: pass
        if self._live_has_q and self._live_stats["tot"]:
            self.live_prog.config(maximum=self._live_stats["tot"], value=self._live_stats["tot"])
        v = {"pass": ("✅ PASS", "good"), "fail": ("❌ FAIL", "bad"), "error": ("⚠ ERRORE", "bad"),
             "skip": ("⏭ SKIP — device non raggiungibile", "warn")}.get(status, (status, "dim"))
        extra = f"   ·   {summary}" if summary else ""
        self.live_verdict.config(text=v[0] + extra, fg=CL[v[1]])
        self.live_badge.config(text=v[0].split()[0], fg=CL[v[1]])

    def _live_idle(self):
        """Nessuna run in corso: indicatore globale a riposo."""
        self._live_active = False
        self.runpill.config(text="○ pronto", fg=CL["muted"])

    def _tick(self):
        """Ogni secondo: fa avanzare orologio/ETA e fa lampeggiare l'indicatore mentre i test girano."""
        if getattr(self, "_live_active", False):
            self._run_blink = not getattr(self, "_run_blink", False)
            self._render_live()
        self.after(1000, self._tick)

    def _live_line(self, tid, line):
        """Una riga di stdout dal test in corso → statistiche in alto + riga colorata nel log."""
        if getattr(self, "_live_tid", None) != tid:
            return
        if self._live_update_stats(line) and not self._live_has_q:
            self._live_has_q = True                     # prima riga per-query: rivela i riquadri + barra a %
            self._cells.pack(side="top", fill="x", padx=8, pady=(2, 4), before=self.live_prog)
            try: self.live_prog.stop(); self.live_prog.config(mode="determinate")
            except tk.TclError: pass
        self._render_live()
        self.out.insert("end", line + "\n", self._live_color(line))
        try:                                  # cap del buffer: i soak NL fanno migliaia di righe
            if int(self.out.index("end-1c").split(".")[0]) > 4000:
                self.out.delete("1.0", "2000.0")
        except (tk.TclError, ValueError):
            pass
        self.out.see("end")

    def _tree_hover(self, e):
        row = self.tree.identify_row(e.y)
        if row == self._tt_row:
            return
        self._tt_row = row; self._hide_tt()
        if row in self.item_for:
            t = self.reg_tests[self.item_for[row]]
            st = self.tests[self.item_for[row]]
            verd = {"pass": "✅ verde", "fail": "❌ rosso", "error": "⚠ errore", "skip": "⏭ saltato",
                    "running": "⏳ in corso", "pending": "○ non eseguito"}.get(st["status"], "")
            tip = (f"{t['label']}   ·   {verd}\n\n{t.get('verifies') or '(nessuna descrizione disponibile)'}"
                   f"\n\nlingua: {t.get('lang', 'na')}   ·   {'fa parte di «ANIMA offline»' if t.get('anima') else 'esclusa da «ANIMA offline»'}"
                   f"\n$ {t['cmd']} {' '.join(t['args'])}\n\nDoppio-click per eseguirlo · click per vederne l'output a destra.")
            self._show_tt(tip)

    def _show_tt(self, text):
        x, y = self.tree.winfo_pointerx() + 16, self.tree.winfo_pointery() + 16
        self._tt = tw = tk.Toplevel(self.tree); tw.wm_overrideredirect(True); tw.wm_geometry(f"+{x}+{y}"); tw.configure(bg="#2a2f3a")
        tk.Label(tw, text=text, bg="#0c0e12", fg="#e6e6e6", font=("Segoe UI", 9), justify="left", wraplength=400, padx=9, pady=7).pack(padx=1, pady=1)

    def _hide_tt(self):
        if self._tt:
            self._tt.destroy(); self._tt = None

    def _show_output(self, _e):
        if self.worker and self.worker.is_alive():
            return                          # una run è in corso: non sovrascrivere lo stream live
        sel = self.tree.selection()
        if not sel or sel[0] not in self.item_for: return
        t = self.tests[self.item_for[sel[0]]]
        self._live_tid = None
        v = {"pass": ("✅ verde", "good"), "fail": ("❌ rosso", "bad"), "error": ("⚠ errore", "bad"),
             "skip": ("⏭ saltato", "warn"), "running": ("⏳ in corso", "warn"),
             "pending": ("○ non eseguito", "dim")}.get(t["status"], (t["status"], "dim"))
        self.live_title.config(text=t["label"], fg=CL["warn"] if self._is_device(t) else CL["fg"])
        self.live_badge.config(text=v[0].split()[0], fg=CL[v[1]])
        self._cells.pack_forget()
        try: self.live_prog.stop(); self.live_prog.config(mode="determinate", maximum=1, value=0)
        except tk.TclError: pass
        self.live_meta.config(text="")
        self.live_verdict.config(text=f"{v[0]}   ·   {t.get('summary', '')}".rstrip(" ·"), fg=CL[v[1]])
        self.out.delete("1.0", "end")
        self.out.insert("end", f"$ {t['cmd']} {' '.join(self._args_for(t))}\n", "dim")
        self.out.insert("end", (t.get("verifies", "") or "") + "\n\n", "dim")
        self.out.insert("end", t["output"] or "(non ancora eseguito)")


