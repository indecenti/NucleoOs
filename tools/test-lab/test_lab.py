    def _write_report(self, ids):
        ids = [i for i in (ids or []) if i in self.tests]
        if not ids: return None
        items = [self.tests[i] for i in ids]
        cats = {t["category"] for t in items}
        scope = ("device" if cats == {"device-load"} else
                 ("full-pc" if len(ids) >= 20 else "partial"))
        cnt = lambda s: sum(1 for t in items if t["status"] == s)
        h = self._compute_health()
        ts = datetime.now()
        report = {
            "ts": ts.isoformat(timespec="seconds"),
            "scope": scope,
            "device_ip": (self._device_ip_snap or None) if "device-load" in cats else None,
            "counts": {"total": len(items), "pass": cnt("pass"), "fail": cnt("fail"),
                       "error": cnt("error"), "skip": cnt("skip")},
            "health": {k: (list(v) if isinstance(v, tuple) else v) for k, v in h.items()},
            "tests": [{"id": t["id"], "label": t["label"], "category": t["category"],
                       "status": t["status"], "summary": t.get("summary", ""),
                       "duration_s": round(t.get("duration", 0.0), 2),
                       "cmd": f"{t['cmd']} {' '.join(self._args_for(t))}"} for t in items],
            # output completo (troncato) SOLO dei falliti: è ciò che serve per diagnosticare a freddo
            "failures": [{"id": t["id"], "label": t["label"], "category": t["category"],
                          "summary": t.get("summary", ""), "output": (t.get("output", "") or "")[-6000:]}
                         for t in items if t["status"] in ("fail", "error")],
        }
        os.makedirs(REPORTS, exist_ok=True)
        stamp = ts.strftime("%Y%m%d-%H%M%S")
        path = os.path.join(REPORTS, f"run-{stamp}.json")
        self._dump_json(path, report)
        self._dump_json(os.path.join(REPORTS, "latest.json"), report)   # puntatore "ultimo" sempre aggiornato
        md = self._report_md(report)
        self._dump_text(os.path.join(REPORTS, f"run-{stamp}.md"), md)
        self._dump_text(os.path.join(REPORTS, "latest.md"), md)
        # indice append-only: una riga per corsa → "qual è l'ultimo" e storico in colpo d'occhio
        try:
            with open(os.path.join(REPORTS, "index.jsonl"), "a", encoding="utf-8") as f:
                f.write(json.dumps({"ts": report["ts"], "scope": scope, "file": f"run-{stamp}.json",
                                    "counts": report["counts"]}, ensure_ascii=False) + "\n")
        except OSError:
            pass
        self._prune_reports()
        return path

    def _report_md(self, r):
        c = r["counts"]
        head = (r["ts"] + (f"  ·  device {r['device_ip']}" if r["device_ip"] else ""))
        L = [f"# Test Lab — {head}", "",
             f"**Ambito:** `{r['scope']}`  ·  ✅ {c['pass']} · ❌ {c['fail']} · ⚠ {c['error']} · ⏭ {c['skip']}  (su {c['total']})", ""]
        hall = r["health"].get("hallucinations")
        if hall is not None:
            L.append(f"**Allucinazioni:** {hall}  {'✓' if hall == 0 else '⚠'}")
        grn = r["health"].get("green")
        if isinstance(grn, list) and len(grn) == 2:
            L.append(f"**Verdi:** {grn[0]}/{grn[1]}")
        L.append("")
        if r["failures"]:
            L.append("## Falliti (per diagnosi)")
            for fdef in r["failures"]:
                tail = next((ln for ln in reversed((fdef["output"] or "").splitlines()) if ln.strip()), fdef["summary"])
                L.append(f"- **{fdef['label']}** ({fdef['category']}) — {tail.strip()[:160]}")
            L.append("")
        else:
            L.append("_Nessun fallimento: tutti i test eseguiti sono verdi o saltati._")
        slow = sorted(r["tests"], key=lambda t: t["duration_s"], reverse=True)[:5]
        if slow and slow[0]["duration_s"] > 0:
            L.append("## Più lenti")
            for t in slow:
                L.append(f"- {t['label']} — {t['duration_s']}s")
        return "\n".join(L) + "\n"

