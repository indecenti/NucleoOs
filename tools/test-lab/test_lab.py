    # ---- metrics + health ----------------------------------------------------------------------
    def _extract(self, tid):
        out = self.tests[tid]["output"] or ""
        vals = {}
        for m in (self.reg_tests[tid].get("metrics") or []):
            try: rx = re.compile(m["re"], re.I)
            except re.error: continue
            ms = list(rx.finditer(out))
            if not ms: continue
            if m.get("agg") == "sum":
                v = sum(sum(int(g) for g in mm.groups() if g and g.isdigit()) for mm in ms)
            else:
                v = int(ms[-1].group(1))
            vals[m["k"]] = vals.get(m["k"], 0) + v
        return vals

    def _compute_health(self):
        per = {}
        for tid in self.tests:
            for k, v in self._extract(tid).items():
                per[k] = per.get(k, 0) + v
        res = {}
        for hd in self.health_defs:
            if hd.get("special") == "green":
                g = sum(1 for t in self.tests.values() if t["status"] == "pass")
                tot = sum(1 for t in self.tests.values() if t["status"] in ("pass", "fail", "error"))
                res[hd["id"]] = (g, tot)
            else:
                res[hd["id"]] = per.get(hd["metric"], 0)
        return res

