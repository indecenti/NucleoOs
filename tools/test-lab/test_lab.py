    @staticmethod
    def _train_parse_count(txt):
        """Quante richieste contiene il testo (array JSON → len; altrimenti righe non-vuote/non-commento)."""
        if not txt.strip():
            return 0
        try:
            d = json.loads(txt)
            if isinstance(d, list):
                return len(d)
        except Exception:
            pass
        return sum(1 for l in txt.splitlines() if l.strip() and not l.strip().startswith("//"))

