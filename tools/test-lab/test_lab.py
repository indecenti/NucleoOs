class Tip:
    """A lightweight hover tooltip (dark, wrapped). Attach to a widget; shows after a short delay."""
    def __init__(self, widget, text, delay=450):
        self.w, self.text, self.delay, self.id, self.tip = widget, text, delay, None, None

    def bind(self, widget):
        widget.bind("<Enter>", self._enter, add="+")
        widget.bind("<Leave>", self._leave, add="+")
        widget.bind("<ButtonPress>", self._leave, add="+")
        return self

    def _enter(self, _e=None):
        self._cancel()
        self.id = self.w.after(self.delay, self._show)

    def _leave(self, _e=None):
        self._cancel(); self._hide()

    def _cancel(self):
        if self.id:
            try: self.w.after_cancel(self.id)
            except Exception: pass
            self.id = None

    def _show(self):
        if self.tip or not self.text:
            return
        x, y = self.w.winfo_pointerx() + 14, self.w.winfo_pointery() + 20
        self.tip = tw = tk.Toplevel(self.w)
        tw.wm_overrideredirect(True)
        tw.wm_geometry(f"+{x}+{y}")
        tw.configure(bg="#2a2f3a")
        tk.Label(tw, text=self.text, bg="#0c0e12", fg="#e6e6e6", font=("Segoe UI", 9), justify="left",
                 wraplength=340, padx=9, pady=7).pack(padx=1, pady=1)

    def _hide(self):
        if self.tip:
            self.tip.destroy(); self.tip = None


