#!/usr/bin/env node
// TYPED-FACET NL gate — runs eval_typed_nl.jsonl through the REAL cascade (anima.exe) and asserts the
// typed-facet tier (nucleo_anima_facet.c) answers correctly AND never fabricates. Case types:
//   occ / gen  -> intent must be "facet" AND the reply must contain the data-true value ("want")
//   died       -> answered (tier != none) AND reply contains the death year ("want")  [via the KGE]
//   abstain    -> the HARD 0-hallucination bar: NO answer (tier none / empty) and intent != facet
//   profile    -> a first-person "my job" overlap must route to the profile tier, not facet
//   answered   -> a "who is X" overlap is answered by some tier but NOT the facet tier
// Exit != 0 on any failure (one bad facet = a hallucination). Run: node tools/anima-host/typed-nl-check.mjs
import { spawnSync } from 'node:child_process';
import { existsSync, readFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const exe = join(here, 'build', 'anima.exe');
// eval_typed_nl.jsonl = data-derived true-positives + curated traps; eval_typegate.jsonl = the
// adversarial type-gate set (generated by the typegate-adversarial workflow). Both, when present.
const files = [join(here, '..', 'anima', 'eval_typed_nl.jsonl'), join(here, '..', 'anima', 'eval_typegate.jsonl')];
if (!existsSync(exe)) { console.error('anima.exe missing — run `npm run anima:build`.'); process.exit(2); }

const items = files.filter(existsSync).flatMap((f) =>
  readFileSync(f, 'utf8').split(/\r?\n/).filter((l) => l.trim() && !l.startsWith('//')).map((l) => JSON.parse(l)));
const fold = (s) => (s || '').normalize('NFD').replace(/[̀-ͯ]/g, '').toLowerCase();

let lang = 'it';
const lines = [];
for (const it of items) {
  lines.push('/reset');
  const want = it.lang === 'en' ? 'en' : 'it';
  if (want !== lang) { lines.push('/' + want); lang = want; }
  lines.push(it.q);
}
const r = spawnSync(exe, [], { input: Buffer.from(lines.join('\n') + '\n', 'utf8'), maxBuffer: 64 * 1024 * 1024 });
const blocks = r.stdout.toString('utf8').split(/^Q: /m).slice(1);
const parse = (b) => ({
  tier: (b.match(/tier=(\S+)/) || [])[1] || 'none',
  intent: (b.match(/intent=(\S*)/) || [])[1] || '',
  reply: ((b.match(/reply: (.*)/) || [])[1] || '').trim(),
});

const fails = [];
for (let i = 0; i < items.length; i++) {
  const it = items[i], p = parse(blocks[i] || '');
  const answered = p.tier !== 'none' && p.reply && p.reply !== '(vuoto)';
  const replyHas = (w) => fold(p.reply).includes(fold(w));
  const refused = replyHas('non un luogo') || replyHas('not a place');   // the type-gate's honest refusal
  let ok;
  switch (it.t) {
    case 'occ': case 'gen': ok = p.intent === 'facet' && replyHas(it.want); break;
    case 'died':            ok = answered && replyHas(it.want); break;
    case 'abstain':         ok = p.intent !== 'facet' && !answered; break;
    // TYPE-GATE: a place-relation about a PERSON must be honestly refused.
    case 'refuse_person':   ok = refused; break;
    // REGRESSION guard: a place-relation about a real PLACE must NOT be refused (that the KGE still
    // ANSWERS it is guarded by route-check/kge-eval; here we only assert the type-gate didn't block it).
    case 'geo_ok':          ok = !refused; break;
    // unknown/non-entity place-relation must abstain (and never be refused-as-person).
    case 'unknown':         ok = !answered; break;
    // place-SHAPED but not a place-relation, or an occupation/gender overlap: the type-gate must NOT fire.
    case 'notrefused': case 'overlap': ok = !refused; break;
    // ambiguous (surname == place): any GROUNDED outcome is fine (refuse / geo answer / abstain); the only
    // failure is dodging with a person BIO or fabricating a place via composition.
    case 'ambiguous':       ok = !(answered && (p.intent === 'l1' || p.intent === 'combinator')); break;
    case 'profile':         ok = p.intent === 'profile'; break;
    case 'answered':        ok = p.intent !== 'facet' && answered; break;
    default:                ok = false;
  }
  if (!ok) fails.push([it.t, it.q, it.lang, it.want || '', `${p.intent || '(none)'}/${p.tier}`, p.reply.slice(0, 50)]);
}

const n = items.length, pass = n - fails.length;
const byT = {}; for (const it of items) byT[it.t] = (byT[it.t] || 0) + 1;
console.log(`[typed-nl] ${pass}/${n} pass  ${JSON.stringify(byT)}`);
if (fails.length) {
  console.log(`FAILURES (${fails.length}):`);
  for (const f of fails) console.log(`  ✗ [${f[0]}/${f[2]}] "${f[1]}" want="${f[3]}" got=${f[4]} reply="${f[5]}"`);
  process.exit(1);
}
console.log('✓ typed-facet NL: all answered correctly, 0 fabrications on adversarial cases');
